package practica;

import java.util.Scanner;

public class EjerCatorce {
	public static void main(String[] args){
		Scanner s = new Scanner(System.in);	
		System.out.println("Ingresar numero");
		int lados, r;
		lados=s.nextInt();
		for (lados=0; lados>1; lados++){
			System.out.print("*");
		}	
		for (r=0; r<lados; r++){
				
		
		System.out.print("\n");
		}	
		
		
	
	}


}


